# In this program we will learn about string slicing

name = "Jarpula Bhanu Prasad"

# The below line will print character from index 7 to character at index 12
print(name[7:13])

# The below line will print the length of the string
print(len(name))

# The below line will print whole string with skip character 
print(name[0::2])

# The below line will print string with negative indices
print(name[-6:])

Name = "Bhanu"
print(Name[-4:-2])
